<script>
import { scrollPercentage, format } from '~/helpers'

export default {
  components: {},
  data() {
    return {
      testing: 'this is my testing',
      layers: [
        { title: 'Foo', author: 'Foo', date: 'Foo', inRange: false },
        { title: 'Foo', author: 'Foo', date: 'Foo', inRange: false },
        { title: 'Foo', author: 'Foo', date: 'Foo', inRange: false },
        { title: 'Foo', author: 'Foo', date: 'Foo', inRange: false },
        { title: 'Bar', author: 'Bar', date: 'Bar', inRange: false }
      ],
      scroll: {
        vector: 0,
        y: 0
      }
    }
  },
  mounted() {
    this.handleScroll()
  },
  created() {
    window.addEventListener('scroll', this.handleScroll)
  },
  destroyed() {
    window.removeEventListener('scroll', this.handleScroll)
  },
  methods: {
    handleScroll(event) {
      this.scroll.vector = scrollPercentage('.substrate')
      this.layers = this.normalizedList
    }
  },
  computed: {
    normalizedList() {
      return this.layers.map((element, index) => {
        const parts = (100 / this.layers.length) * index
        if (this.scroll.vector <= parts) {
          element.inRange = 20
        } else {
          element.inRange = 0
        }

        return element
      })
    }
  }
}
</script>

