      <!-- <div class="oa_footnote_marker"></div> -->

      <p>
        To start generally: an acre, unlike a meter or a foot, is a unit of
        land-area and not a unit of linear measure.
      </p>

      <p>
        The designated area that came to be an acre, was defined during the
        middle ages as the amount of land that could be ploughed in one day by
        one man and one ox<span class="oa_footnote" id="oa_footnote_01">1</span>
        <span class="oa_footnote_entry" id="oa_footnote_entry_01">
          <span class="oa_source">The Calculator Site:</span><br />
          <a
            target="_blank"
            href="http://www.thecalculatorsite.com/articles/units/how-big-is-an-acre.php"
            >How Big is an Acre? Explained.</a
          >
        </span>
        or, as the 1 area created by that effort is commonly referred to, one
        chain by one furlong. Usually a long narrow rectangle, since turning the
        plough often was complicated.<span
          class="oa_footnote"
          id="oa_footnote_02"
          >2</span
        >
        <span class="oa_footnote_entry" id="oa_footnote_entry_02">
          <span class="oa_source">Wikipedia:</span><br />
          <a target="_blank" href="https://en.wikipedia.org/wiki/Acre">Acre</a>
        </span>
      </p>

      <p>
        The acre then, was first an action that after repetition came to be a
        standardised area. Not a lot of spaces come into existence in that way.
        The online space and the outer space, come in mind as other possible
        examples.
      </p>

      <p>
        This recently came to mind when reading Kenneth Chang&rsquo;s
        article<span class="oa_footnote" id="oa_footnote_03">3</span>
        <span class="oa_footnote_entry" id="oa_footnote_entry_03">
          <span class="oa_source">The New York Times:</span><br />
          <a
            target="_blank"
            href="https://www.nytimes.com/2017/02/22/science/trappist-1-exoplanets-nasa.html"
            >7 Earth-Size Planets Orbit Dwarf Star, NASA and European
            Astronomers Say</a
          >
        </span>
        about the discovery of the seven new planets orbiting a dwarf star named
        Trappist-1, in an effort to convey their size, he wrote:
      </p>

      <p class="oa_quote">
        &ldquo;If placed within our solar system, all seven would lie within the
        orbit of Mercury, and they circle the star quickly. The innermost
        completes an orbit in just 1.5 days. The farthest one completes an orbit
        in 12.35 days. That makes the planetary system more like the moons of
        Jupiter than a larger planetary system like our solar system.&rdquo;
      </p>

      <p>
        59 words that describe the completely unfamiliar with the slightly less
        familiar, as we try to picture what it would look like to lie within the
        orbit of Mercury. If we succeed we are rewarded with a feeling or an
        understanding &mdash; always in scale &mdash; because the actual size is
        just unimaginable.
      </p>

      <p>
        Having echoing characteristics in their unimaginable size and tendency
        to always be addressed (size-wise) in metaphors or analogies, the online
        terrain almost competes in how imperceptible its size actually is.
      </p>

      <p>
        It seems that spaces of such magnitude and unmeasurable area, need to be
        felt out,<span class="oa_footnote" id="oa_footnote_04">4</span>
        <span class="oa_footnote_entry text" id="oa_footnote_entry_04"
          >Silvio Larusso helped in thinking the right verb for the action of
          measuring online space within the acre analogy, he suggested the
          Italian phrase “andare a tentoni” that literally translates in english
          “to grope”, to search blindly or uncertainly by feeling with the
          hands. But grope’s connotations seemed in part non-fitting so we went
          with “feel something out” as in investigate something cautiously or be
          aware of (a person or object) through touching or being touched. We
          particularly liked the possibility of online terrain touching
          back.</span
        >
        like the exploration of a room in the dark, hands and actions first. and
        then maybe a standardized unit of linear measure, or even better of
        land-area can come.
      </p>

      <p>
        The pixels, MBs and GBs, lines of code and page views that are currently
        used to measure out the online terrain, say very little about the online
        ploughable space and the people and oxen &mdash; or more appropriate
        bots &mdash; that scroll/crawl them into harvest.
      </p>

      <p>
        You plough online acres by scrolling through them, at times stopping to
        perform a click, and rearrange your plough, for a new long narrow
        rectangle to be presented for cultivation by you and your mouse, or
        trackpad, or finger; and as acres are, the area remains the same, even
        if the shapes and forms vary from website to website.
      </p>

      <p>
        Perhaps then that should come to be the unit of measure of online space.
        The amount of distance &mdash; or more likely area &mdash; scrollable by
        one person and one device in the course of one day. Would that then fall
        into the roam of imperceptible or unimaginable, well that remains to be
        seen. The repeated action needs to be payed attention first.
      </p>

      <p>
        This space was assembled to pay attention to the action of scrolling as
        a potential unit of measure of online terrain. We built this acre that
        currently expands and moulds as you scroll through it. Where through the
        action, and the action&rsquo;s repetition by each visitor after you, we
        will be able to feel out collectively how much an acre online is. We are
        gonna spend the time to feel out this unimaginable online space, and
        then we will start building bottles for texts, the first to come in
        December 2017.
      </p>

      <p>
        Our interest in making bottles comes from the traditions of Virginia
        Wolf&rsquo;s Glossary for reinventing english and Ursula K. Le
        Guin&rsquo;s Carrier Bag Theory of Fiction. That follow a line of leafs,
        gourds, shells, nets, bags, slings, sacks, bottles, pots, boxes, of
        containers and if I may add of places that hold things. We are conscious
        that actions, tend to bring heroes and we too, are uninterested in them.
      </p>

      <p>
        We would like to hold worlds, like books hold words. But forth most
        avoid copy-pasting books to the internet. To perhaps
        &ldquo;reinvent&rdquo; what a bottle means in respect to online, a
        holder of whatever could be considered liquid online. Yet we have no
        interest in reinventing a medium or an industry.<span
          class="oa_footnote"
          id="oa_footnote_05"
          >5</span
        >
        <span class="oa_footnote_entry text" id="oa_footnote_entry_05"
          >A list of a few places that come in mind: letterpress printing,
          Kickstarter, Blogger, Twitter, HTML, hypertext, Kindle, epubs, Medium,
          and so on and so forth.</span
        >
        We have an interest of building bottles that align with holodecks,<span
          class="oa_footnote"
          id="oa_footnote_06"
          >6</span
        >
        <span class="oa_footnote_entry text" id="oa_footnote_entry_06"
          >This thinking of making bottles that align with holodecks was
          inspired by mixing, in parts the forth mentioned essay by Ursula K. Le
          Guin and in parts by paraphrasing, reversing and placing in other
          topic the fist line of Anthony Antonellis’ Internet Art in a Gallery:
          “Don’t copy paste internet art into a gallery. Make a holodeck.”</span
        >
        to hold texts that can&rsquo;t be held anywhere else.
      </p>

      <p>
        We have this idea, that our bottles could belong to the reader, like
        books belong to a reader, to lend and give away, to write notes in their
        margins, and yet to maintain them online. We are interested in borrowing
        from publishing &mdash; as in making public by dispersing copies as Paul
        Soulelis writes<span class="oa_footnote" id="oa_footnote_07">7</span>
        <span class="oa_footnote_entry text" id="oa_footnote_entry_07"
          >Paul Soulelis explored this idea on
          <a href="http://rhizome.org/editorial/2015/nov/12/the-download/"
            >The Download</a
          >, a series of Rhizome commissions that considers the posted files,the
          act of downloading, and the user’s desktop as the space of exhibition,
          with an intent to go beyond browsing.</span
        >
        but yet remain online, within in our ploughed online acre: a website,
        build as a bottle, each time for each text-based work to come.
      </p>

      <p>But more on that later.</p>

      <p>For now, an online acre to be ploughed.</p>
