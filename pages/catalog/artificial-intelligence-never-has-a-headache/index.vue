              consists of building structures, patterns and templates. During
              the three years I have been studying this discipline, I became
              fascinated with the ways designers work with
              <span class="added">content</span>
              <span class="removed">information</span>: organizing enormous
              amounts of data and creating complex schemes to sort, digest, and
              display it. Reducing information flows to a rather visual message
