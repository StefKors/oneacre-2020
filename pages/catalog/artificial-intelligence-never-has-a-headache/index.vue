            <p>
              Researching the milestones in the development of AI taught me that
              since the 1960’s AI has been trained to have conversations with a
              human, and to compete in games. Comparing the approach of
              biohackers with Donna Haraway’s cyborg manifesto allows us to
              understand the origins of data-driven lifestyle: It comes from
              technology enthusiasts on one side, and the values of capital and
              the need to compete on a job market on the other side. The desire
              to compete fuels the productivity race, and the fear of AI as our
              competitor makes people feel helpless.
            </p>

            <p>
              The rising popularity of wearables and activity trackers increases
              the feeling that a human body “as is” is insufficient. These tools
              contribute to the feeling of insecurity not only by their presence
              in our lives, but also by the ways they operate: Lifelogging
              craziness can be seen as a never ending cycle — users track their
              health data in order to not get sick, because they need to stay
              healthy to be productive so they can work to afford this
              technology which helps them not to get sick...The culture of high
              achievers has no place for insecurity.
            </p>

            <p>
              Interfaces of messenger apps make conversation with a human and
              conversation with a chatbot look alike. The need for constant
              interaction is partly created and partly managed by software —
              people talk to each other via applications, which are shaping the
              conversation in such a way that it is not that important whether
              there is anyone on the other side of the screen.
            </p>

