              focused on removing the stressful part of making a decision? Maybe
              there should be a app for that?
            </p>

            <p>
              When looking at the existing iOS lifestyle applications, I see two
              extremes: There are so-called productivity apps, with goals to
              stimulate the user to achieve the desired result. There are
              planners, somewhere in between two extremes, because they are
              neutral towards the achievement of goals but also have a system of
              reminders to constantly keep on track with the status of the task.
              On the other hand, there are meditation and relaxation apps, meant
              to reduce anxiety, created by productivity tools and the constant
              buzz of reminders.
            </p>

            <p>
              Isn’t it bizarre that there are applications to manage one’s
              anxiety in the environment where reminders, timers, trackers and
              updates are inducing it at the very same time?
            </p>

            <p>
              By embracing the development of our operational systems and
              upgrading our devices, we are approaching technological
              singularity. We trust our planners and apps to help us decide how
              to live. Will the machine help me to sort out my issues, will
              technology be my therapist? In a perfect world I can be human and
              do my "human thing” all while letting the machine organize me.
            </p>

            <p>
              The illusion of human to human interaction with the machine is
              called the ELIZA effect
              <sup>17</sup>. ELIZA is a computer program, written at MIT by
              Joseph Weizenbaum between 1964 and 1966. It was designed to
              simulate a human-like conversation, based on processing user
              responses to scripts. The script DOCTOR was designed to simulate a
              conversation with a psychotherapist. It was one of the first
              chatterbots. Chatterbot (or bot) is a program whose purpose is to
              have a conversation with a human. The perspective of such software
              was that a computer would be able to pass a Turing test not by
              acquiring human qualities and intelligence, but by simply fooling
              the human.
            </p>

            <p>
              ELIZA is a milestone in the development of artificial intelligence
              because it was the first time that the goal of a programmer was to
              create an illusion of human-human interaction. Weizenbaum noticed
              that humans tend to bond emotionally with the machines, and unlike
              a car or a musical instrument, a computer affects the thinking and
              behaviour. In the 1976 book
              <i>Computer Power and Human Reason</i>
              he writes about people who experienced emotional bonding with the
              program he wrote:
            </p>

            <p>
              <span class="quote">
                “I was promptly bombarded with accusations that what I proposed
                amounted to spying on people’s most intimate thoughts; clear
                evidence that people were conversing with the computer as if it
                were a person who could be appropriately and usefully addressed
                in intimate terms. I knew of course that people form all sorts
                of emotional bonds to machines, for example, to musical
                instruments, motorcycles, and cars. And I knew from long
                experience that the strong emotional ties many programmers have
                to their computers are often formed after only short exposures
                to their machines. What I had not realized is that extremely
                short exposures to a relatively simple computer program could
                induce powerful delusional thinking in quite normal people. This
                insight led me to attach new importance to questions of the
                relationship between the individual and the computer, and hence
                to resolve to think about them.”
                <sup>18</sup>
              </span>
            </p>

            <p>
              In 1972, ELIZA had the first computer to computer conversation
              with an artificial intelligence program named PARRY. Eliza was
              simulating a doctor, and Parry was built to simulate a patient,
              suffering from Schizophrenia.
            </p>

            <p>
              The machines we get attached to technically are still machines —
              by acquiring intelligence it does not acquire organic nature or
              bodily awareness. In order to understand why we rely on technology
              in our routine, we need to know and remember that the machine can
              simulate understanding and be perceived as a human.
            </p>

            <p>
              Why does this shift became possible only with digital technology?
              In the same article Weizenbaum explains:
            </p>

            <p>
              <span class="quote">
                “...What is it about the computer that has brought the view of a
                man as a machine to a new level of plausibility? Clearly there
                have been other machines that imitated man in various ways, e.g,
                steam shovels. But not until the invention of the digital
                computer have there been machines that could perform
                intellectual functions of even modest scope: i.e., machines that
                could in any sense be said to be intelligent. Now “artificial
                intelligence” (AI) is a subdiscipline of computers science. This
                new field will have to be discussed. Ultimately a line dividing
                human and machine intelligence must be drawn. If there is no
                such line, then advocates of computerized psychotherapy may be
                merely heralds of an age in which man has finally been
                recognized as nothing but a clock-work. Then the consequences of
                such a reality would need urgently to be divided and
                contemplated.”<sup>19</sup>
              </span>
            </p>

