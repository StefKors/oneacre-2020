            <!-- <%# Side note: The Pinterest experience %> -->
            <div class="side_note">
              <p>Branch 003</p>
              <p>
                Online platforms such as Pinterest already give us advice on
                what to wear, what to cook, how to decorate our houses and many
                other topics. An example of embedding the non-curated selection
                in daily life is following trending topics on such online
                archives. Pinterest is a collection of boards where users “pin”
                contents from all over the web, creating a huge web archive.
              </p>

              <p>
                One day a BuzzFeed journalist, named Rachel Wilkerson Miller,
                decided to live a week of her life, according to the “most
                popular” page on Pinterest. She wrote:
              </p>

              <p>
                <span class="quote">
                  “Since its launch in 2010, Pinterest has earned a reputation
                  as a site for Mormon housewives, mommy bloggers, and basic
                  white girls. I am a woman of color with a full-time job, I
                  spend less than 30 minutes getting ready in the morning, and I
                  still like Pinterest. Characterizations of the site as a “a
                  churning cycle of interest, hope, inspiration, jealousy,
                  desperation, despair and depression” always irked me because I
                  think Pinterest is a useful bookmarking tool. The site had
                  never made me feel bad about myself. Then I discovered
                  Pinterest’s “most popular” page, which is essentially a
                  collage of white girls with impossibly great hair, superhuman
                  nail art skills, and apparently enough free time to create a
                  tidy basket of “postpartum supplies” for “every bathroom” in
                  the house. Suddenly I could see where Pinterest got its
                  reputation.”<sup>30</sup>
                </span>
              </p>

              <p>
                Designed as a bookmarking tool, Pinterest creates an image
                moodboard for a user based on her own interests, but since the
                online realm doesn’t include failures and disappointments as in
                the offline routine, it presents you an image of yourself
                (reflected in in interests), but better. If looking for a less
                personalised result in favour of understanding trends based on
                the content on the entire platform, one can check the “popular”
                pins and boards. Fitting in the online trend, Wilkinson Miller
                discovered that trending advice looks completely alien to her.
              </p>

              <p>
                Until reading this article it never appeared to me that my
                personalized search does not give any idea of the main content
                of the resource. When I open the most popular pins collection on
                September 29, 2015, I saw a selection of DIY and handmade
                projects and felt like I’d just discovered a new online
                platform. My own feed consists of posters, vegan food, UX design
                and other things I find interesting. The world of face
                contouring and crocheted everything is as alien to me as to
                Wilkerson Miller, even though we are both Pinterest users. The
                Pinterest community feels non-curated and rather impersonal, but
                highly visually saturated — pinners form giant arrays of images,
                creating a collective (unconscious?) visual identity. Of course,
                trends emerge because many users like them, and that makes the
                sorting mechanism present them as popular. It is not the same as
                asking an app about what to do with your life.
              </p>

              <p>
                One of the successful tips that Wilkerson Miller took from
                Pinterest was making salads in a jar. Mason jar salads used to
                be a hipster food, and maybe still are, but I do wonder if the
                amount of re-pins of the recipes and tutorials tricked the
                stores into adding salad jars into their assortment, and if the
                people who buy salad jars learned about the trend from
                Pinterest.
              </p>
