            <div class="side_note">
              <p>Branch 004</p>
              <p>
                Previously I have mentioned ELIZA, a chat bot which simulated a
                human-human interaction. “The invisible boyfriend”
                <sup>35</sup>
                is an interesting example of further development in a concept of
                virtual presence. It is a human-to-human interaction which is
                curated by an interface. This service is created to satisfy the
                needs of people who, for various reasons, need to simulate being
                in a relationship. This service creates a realistic virtual
                presence and a detailed documentation of a non-existent
                relationship, so the client can make others believe that he or
                she is dating someone.
              </p>

              <p>
                The curious detail of this app is the fact that although the
                search is automated both the client and the person who works as
                the “invisible boyfriend” are real people. The article “With
                Bots Like These, Who Needs Friends?”
                <sup></sup>
                by Tim Moynihan of Wired explains the process and the history of
                creation of the Invisible Boyfriend:
              </p>

              <p>
                <span class="quote">
                  “The concept is simple: You pay to bae. The apps are run by
                  Matt Homann and Kyle Tabor, who cooked up the idea during a
                  hackathon in 2013. The service began as a chatbot simulation
                  wherein you would text a “boyfriend” or “girlfriend,” but now,
                  actual human beings are doing the talking. Tabor says more
                  than 70,000 fake girlfriends and fake boyfriends have been
                  created since the service launched in January, proving there
                  is an economy of loneliness.
                  <br />
                  <br />
                  The basics to the service (picking a name, a photo, an age,
                  etc) are free. If you want to take things any further, it’ll
                  cost you. For $25 per month, you get 100 text messages, 10
                  voicemails, and a handwritten note from your fake boo. $15 per
                  month gets you texts only.”
                </span>
              </p>

              <p>
                The initial idea of creating a chatbot didn’t create a
                convincing simulation, so the company hired real people through
                Crowdsource. According to the founders, currently there are over
                600 people writing for the company.
              </p>
