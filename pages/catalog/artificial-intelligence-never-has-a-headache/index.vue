            <p>
              Biohacking is a movement that, among its other scopes, is
              dedicated to augmenting nature and the human body in particular by
              the means of technology. It aims to improve functionality and
              performance and also to provide people with more freedom in
              designing their own lives not by managing them, but by adjusting
              the body to the tasks it needs to perform. The data-driven
              lifestyle, first adopted by a few technology and computing
              enthusiasts, is getting more and more followers. Using hacker
              ethic,
              <sup>20</sup>
              biohackers consider the human body and mind a project which can be
              developed further.
            </p>

            <p>
              Hacker ethic is a term for the moral values and philosophy that
              are common in hacker culture. Practitioners of the hacker ethic
              acknowledge that sharing information and data responsibly is
              beneficial and helpful. Whilst the philosophy originated at the
              Massachusetts Institute of Technology in the 1950s–1960s, the term
              hacker ethic is attributed to journalist Steven Levy as described
              in his 1984 book titled
              <i>Hackers: Heroes of the Computer Revolution</i>. The key points
              within this ethic are access, freedom of information, and
              improvement to quality of life.
              <sup>21</sup>
            </p>

            <p>
              From their own point of view on the freedom of information ,
              biohackers claim access to biology/our bodies/data which can be
              received from self-tracking. The ideology stands for taking
              control of one’s health and mental condition from the hands of
              doctors and therapists. It promotes educating oneself about the
              way our bodies work and using intelligence, technology and logic
              to become an “advanced model” of an ordinary individual.
