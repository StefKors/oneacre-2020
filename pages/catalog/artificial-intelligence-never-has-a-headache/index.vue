              product of balancing two extremities: making fast decisions
              without contemplation, and taking a very long time, and a lot of
              effort to make the best choice, which may lead to paralyzing
              indecisiveness.
            </p>

            <p>
              The number of tools is escalating, so the search of balance for
              the sake of productivity helps spawn more and more apps in the
              already-saturated market. Instead of modifying my routine in order
              to enable it to structure itself, I get clogged in the labyrinth
              of various methods and solutions.
            </p>

            <p>
              Numerous planners and trackers remind me that I have serious tasks
              to complete and deciding and actually doing things becomes a
              burden. In Homo Ludens, Johan Huizinga writes that the highest
              concentration and dedication to the process happens if the process
              is wrapped in the structure of a game. Paradoxically, playing
              becomes the most serious process. The players are aware that they
              are playing a game, but instead of being weighed down by
              decision-making, they concentrate on an actual task.
            </p>

