            <div class="side_note" id="move">
              <p>Branch 002</p>
              <p>
                In the 1964 book Understanding Media, Marshall McLuhan wrote
                that all tools of communication are an extension of our body
                <sup>7</sup>
                . Counting all the technological development since the sixties,
                when the book was written, it is logical to think of a next step
                not as a further extension, but as a shift.
              </p>

              <p>
                If the physical body is not a necessity anymore, what kind of
                functionality will define a human being and how we will make our
                intelligence stand out?
              </p>

              <div class="oa_commit note">Commit:<span>2018</span></div>
              <div class="oa_commit_entry">
                <p>And is there a need to stand out?</p>
              </div>

              <p>
                We are Homo sapiens, “the wise men”, the people who know.
                Knowledge is subjective. On the other hand, the technology we
                have created with our wisdom, provides us with the objective
                information we are missing. With the idea of competition in
                mind, when the human is compared to a machine, the understanding
                of the strong points of humans as a species is not as strong as
                the understanding of their weak points. In the current intuitive
                and responsive digital workflow, computers trick humans into
                believing they are the same, and humans try to pick up the pace
                and to “adapt for the machinery”
                <sup>8</sup>
              </p>

              <p>
                The attempt to imagine the future network of organic and
                non-organic intelligences is not just a speculation. It is a
                tool, a filter, which helps to reflect on the ways in which
                people interact with their devices
              </p>

