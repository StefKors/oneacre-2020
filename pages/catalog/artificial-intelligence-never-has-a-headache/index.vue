            <h4>1. DEFINITIONS</h4>

            <p>
              Before begging to investigate, I need to define the difference
              between an organism (because human is an animal, and therefore, an
              organism) and a machine, and between natural and artificial.
              “Artificial intelligence” should also be defined.
            </p>

            <p>
              The term “organism”
              <sup>9</sup>
              comes from a Greek word organon, which literally means
              “instrument”. The living thing that can react to stimuli,
              reproduce, grow and maintain homeostasis. The Wikipedia article
              describes homeostasis as
            </p>

            <p>
              <span class="quote"
                >“ ...the property of a system in which variables are regulated
                so that internal conditions remain stable and relatively
                constant. Examples of homeostasis include the regulation of
                temperature and the balance between acidity and alkalinity (pH).
                Human homeostasis is the process that maintains the stability of
                the human body's internal environment in response to changes in
                external conditions.”
                <sup>10</sup>
              </span>
            </p>

            <p>
              In other words, organisms are able to regulate their internal
              processes and to adjust to the environment. An organism maintains
              its bits and parts together and positions itself in space, being a
              <i>self-regulating design</i>. An organism can re-grow some of its
              parts. For example, the average person, an organism, loses
              somewhere in between 60 and 100 hairs a day, and 90% of all hairs
              on her head are in the stage of growth. The remaining 10 percent
              are in the stage of resting and about to fall.
            </p>

            <p>
              Speaking of homeostasis, devices have enclosures designed in a
              such a way that people can use them without breaking the
              mechanism. I may think that AI is not aware of its own enclosure
              and physical presence, but I barely know anything about the
              processes inside my own body. Although people are aware of having
              a body, they often face difficulties when describing what exactly
              they feel.
            </p>

            <p>
              According to Stanford Encyclopedia of Philosophy we are aware of
              “the position and movements of our limbs, of the contact of our
              clothes on our skin, of the muscle pain in our legs, of our
              feeling of thirst. It may be difficult to describe what we feel,
              but what we feel can be so intense that we are sometimes not able
              to think of anything else than our body”
              <sup>11</sup>.
            </p>

            <figure class="oa_img">
              <img src="/images/karina/karina_img/4.jpg" alt="" />
              <figcaption>
                [E] Vitruvian Man, Leonardo da Vinci, 1492
              </figcaption>
            </figure>

            <p>
              Machines are not organically constructed, but man-made. They are
              the products of human culture; so far they remain inside of it,
              and there is no “machine culture” yet. Also, there are no machines
              which are able to think exactly the way humans do, because the
              knowledge about the brain doesn’t give all the information,
              required to re-create these processes. In school, we are taught
              the basics of computation and languages, but the way the brain
              multiplies or subtracts or conjugates verbs remains unexplained.
            </p>

            <p>
              The machine can be reassembled, or if destroyed, replaced with
              another one. The production of a machine follows very strict
              guidelines, and the result is predictable. This is the opposite of
              organic nature, where the result varies. Genetically modified and
              edited organisms are the result of a human attempt to make
              organisms which fit certain expectations, and the difference
              between production of an Apple and production of an apple gets
              blurred.
            </p>

            <p>
              A machine has mechanical power, is built to perform particular
              tasks, and can be turned on and off. A human cannot be turned on
              and off entirely, only certain functions can be enabled or
              disabled. Modern medicine works towards the possibility to extend
              a human lifetime by making parts replaceable or growing new ones.
              The desire for eternal life has been clearly expressed by certain
              groups — from the believers in afterlife to the adepts of
              cryonics, who freeze their brain hoping that one day there can be
              a technology to revive it and give it a body.
            </p>

            <p>
              Google, in the meantime, does not only count on its machinery, but
              has established a biotech company. Calico
              <sup>12</sup>
              (California Life Company), founded in 2013, researches aging in
              order to enable people to lead longer and healthier lives.
            </p>

            <p>
              Certainly, technological progress has a lot to do with the fear of
              death, something which will inevitably happen to a wealthy
              first-world tech-optimist, as well as to anyone. As a living
              being, which has begun and will end and then decompose, a human
              has only one body and cannot (yet) transition to another one, only
              adjust the one she already owns. Decomposing organisms fertilize
              the growing ones, unlike the decomposing machines. Would the world
              be a better place if we could grow hard drives out of piles of
              dead floppy disks?
            </p>

            <p>
              In his book
              <i>Design as Art</i>
              Bruno Munari describes an orange as an industrial product. After
              explaining the structure and the content of an orange, he explains
              the purpose of the seed:
              <span class="quote">
                “...the sections generally contain a small seed from the same
                plant that produced the fruit. This is a small free gift offered
                by the firm to the client in case the latter wishes to start a
                production of these objects on his own account. We draw your
                attention to the fact while no economic loss is incurred in this
                gift, it gives rise to an important psychological bond between
                producer and consumer...”
                <sup>13</sup>
              </span>
            </p>

            <p>
              Ironically, one of the most influential tech companies of our time
              carries a name of a fruit. Imagine Apple giving a seed of an
              IPhone to its customer?
            </p>

