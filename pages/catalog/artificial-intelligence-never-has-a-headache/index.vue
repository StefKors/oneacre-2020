            <p>
              Back to the topic of intelligence, I will cite Steven Pinker’s
              book
              <i>How The Mind Works</i>:
              <span class="quote"
                >“Intelligence, then, is the ability to attain goals in the face
                of obstacles by means of decisions based on rational
                (truth-obeying) rules. The computer scientists Allen Newell and
                Herbert Simon fleshed this idea out further by noting that
                intelligence consists of specifying a goal, assessing the
                current situation to see how it differs from the goal, and
                applying a set of operations that reduce the difference. Perhaps
                reassuringly, by this definition human beings, not just aliens,
                are intelligent. We have desires, and we pursue them using
                beliefs, which, when all goes well, are at least approximately
                or probabilistically true. — intelligence is following the
                desire, but what does the AI follow?”
                <sup>14</sup>
              </span>
            </p>

            <p>
              Pinker notes that rational rules are described as truth-obeying.
              Machine intelligence is not meant to have doubts or to modify what
              “the truth” is. Serendipity, as a quality of the human thinking
              process, seems to be a limitation when the only criterion of
              comparison is efficiency. People are familiar with doubts and
              paradoxes, and intuition can lead to a new insight. But, being a
              human myself, I have no objectivity in defining whether the
              serendipitous detours and procrastination are not the part of a
              “truth-obeying” script running on the background<sup>15</sup>.
              Even more, as I evolve as a professional, I try to incorporate
              failures and hesitations into the process of aiming for
              hyperefficiency.
            </p>

            <p>
              Counting on the latest developments in machine learning, the fear
              of AI is the fear of machines having their own truth, which may
              not include the wellbeing of humanity.
            </p>

