            <div class="side_note" id="cover">
              <p>Branch 001</p>
              <p>
                In 1982 Ridley Scott’s masterpiece “Blade Runner”
                <sup>4</sup>
                depicts a world where humans coexist with androids, manufactured
                by the powerful Tyrell corporation. The corporation's motto,
                “More human than human”, is a dream-come-true AI challenge:
                competing with a human player is an ultimate training playground
                for the development of artificial intelligence.
              </p>

              <p>
                In 1997, a computer called Deep Blue defeated the reigning chess
                champion. In less than 20 years the next milestone in AI
                development was reached: in October 2015, Google’s AlphaGo
                defeated the reigning three-time European Go champion Fan Hui.
              </p>

              <div class="oa_commit note">Commit:<span>2018</span></div>
              <div class="oa_commit_entry">
                <p>
                  "Einfach nerdig, a Youtuber with currently only one video up,
                  started a livestream of an AI learning to play “Super Mario
                  Bros.” 4 days ago. It’s still running, and watching it is
                  amazing."
                  <a
                    href="https://thenextweb.com/artificial-intelligence/2018/01/03/this-live-stream-of-ai-learning-to-play-super-mario-bros-is-awesome/"
                  ></a>
                </p>
              </div>

              <p>
                The difference between Deep Blue and AlphaGo is that the latter
                taught itself. Calculating the most efficient combination, as in
                chess, would not have been possible, since in the game of Go
                there are
                1,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000,000
                possible positions.
              </p>

              <figure class="oa_img">
                <img src="/images/karina/karina_img/2-a.jpg" alt="" />
                <figcaption>
                  [B] Deep Blue, a computer similar the one which defeated
                  champion Garry Kasparov in May 1997. It is the first computer
                  to win a match against a world champion.
                </figcaption>
              </figure>
              <figure class="oa_img">
                <img src="/images/karina/karina_img/2.jpg" alt="" />
                <figcaption>
                  [C] Garry Kasparov playing against the computer program Deep
                  Junior in January 2003.
                </figcaption>
              </figure>
              <p>
                In 2016, Demis Hassabis, the CEO of Google’s Deep Mind,
                published an article, explaining the process to a general
                audience:
                <span class="quote">
                  We built a system, AlphaGo, that combines an advanced tree
                  search with deep neural networks. These neural networks take a
                  description of the Go board as an input and process it through
                  12 different network layers containing millions of neuron-like
                  connections. One neural network, the “policy network,” selects
                  the next move to play. The other neural network, the “value
                  network,” predicts the winner of the game. We trained the
                  neural networks on 30 million moves from games played by human
                  experts, until it could predict the human move 57 percent of
                  the time (the previous record before AlphaGo was 44 percent).
                  But our goal is to beat the best human players, not just mimic
                  them. To do this, AlphaGo learned to discover new strategies
                  for itself, by playing thousands of games between its neural
                  networks, and adjusting the connections using a
                  trial-and-error process known as reinforcement learning. Of
                  course, all of this requires a huge amount of computing power,
                  so we made extensive use of Google Cloud Platform.”
                </span>
              </p>

              <p>
                The way in which Hassabis formulates the goals AlphaGo is about
                to achieve can perfectly explain how AI can acquire a passion
                for competing. In his words, the goal is to “beat the best human
                players, not just mimic them”. To process this information as a
                non-scientist, I allow myself to generalize and conclude that
                Google is building an enormous artificial brain and teaching it
                to win at games, using games as a playground to train the
                superintelligence until it will be applied to solving “important
                real-world problems”.
              </p>

              <figure class="oa_img">
                <img src="/images/karina/karina_img/3.jpg" alt="" />
