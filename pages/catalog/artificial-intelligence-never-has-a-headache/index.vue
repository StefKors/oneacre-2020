              <p>
                In 1938, in Homo Ludens: a study of the play-element in culture,
                Johan Huizinga writes that as a game can be seen as a
                battlefield, the battlefield can also be seen as a game.
                According to Huizinga, playing games is fundamental for humanity
                and vital for animals: While human kids play with dolls and fake
                guns, kittens attack their toys and chase each other. Once a
                human grows old and becomes serious, activities which have no
                practical purpose become luxury.
                <sup>6</sup>
              </p>

