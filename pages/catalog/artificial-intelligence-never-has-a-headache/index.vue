            <h4>6. Liberating randomness</h4>
            <%# 6. Liberating randomness %>

            <p>
              There are programs and applications, employed as personal
              assistants. Personal assistants are not something everyone can
              afford, but in the culture of high achievers people are so busy
              that it is necessary to have one. Resolving issues and managing
              routines with ease has become a requirement and created a whole
              new level of complex workflows. It takes time and effort to manage
              and plan, and these tasks are taken seriously.
            </p>

            <p>
              The popularity of random generators comes as a counter-argument in
              this technological craziness. There are elements of interface
              design, entirely dedicated to creating serendipity — the famous
              “Shuffle” function is an example, or an option to read a random
              article on Wikipedia. In the environment which is so well
              customised for user’s needs, the elements of randomness are gone:
              no more zapping tv channels, no more radio stations, or playing
              all these songs you would never expect to hear.
            </p>

            <figure class="oa_img">
              <img src="/images/karina/karina_img/9.png" alt="" />
              <figcaption>
                [J] Photo of the iPod video, iPod nano 2GB andiPod shuffle 2GB
              </figcaption>
            </figure>

            <p>
              Tools which are meant to structure and organize the process of
              choosing are becoming obstructions: it is harder to make a choice
              while being presented with so many options and possibilities to
              calculate the result. Making a solemn decision becomes a burden.
              The increasing notion of responsibility is frustrating.
            </p>

            <p>
              People are tricked by the idea that the knowledge of a problem
              that a human mind cannot seize in one go can be compressed. A
              famous painting by Magritte states that it is not a smoking pipe.
              It is a painting. The canvas and layers of paint on it have
              nothing to do with smoking. A map of the world is nothing but a
              piece of paper with colors and lines and letters on it. But when
              we look at the map, we see continents and oceans.
            </p>

            <p>
              We draw and imagine an ocean while filling in our agendas,
              planning our lives. Digital technology allows us to scale, and to
              drag and drop things in our oceans. One of the features of using
              applications for managing tasks and helping with choices is that
              the element of a game is introduced in the routine. Instead of
              thinking of the final goal the interface presents you the score.
              Information which would be difficult to digest otherwise is
              presented conveniently.
            </p>

            <p>
              In her lecture
              <i>Great design is serious, not solemn</i>
              Paula Scher talks about the different levels of concentration.
              Being solemn, in her words, is completing a task with the idea of
              its importance in mind. It is like being an adult. But being
              absolutely serious is being immersed in the process as a child,
              immersed in a game.<sup>38</sup>
            </p>

            <div class="oa_commit note">Commit:<span>2018</span></div>
            <div class="oa_commit_entry">
              <p>
                As a designer, I would be so so happy to follow Paula’s speech
                and to produce work in a joyful flow, but how to achieve the
                joyful flow when you constantly need to plan, schedule, write
                bills, have meetings, do tax reports, apply for visas etc…
              </p>
            </div>

            <p>
              Great design, she says, is achieved by being serious, and on a
              larger scale that means that we achieve our best results when we
              forget that we are playing the game, but adopt the game as a
              current state of living.
            </p>

            <p>
              Designed to simplify our life, productivity and lifelogging tools
              also confuse the process of choosing and increase the amount of
              decisions we make without thinking. These systems execute a
              variety of tasks, following the concept of making users everyday
              life easy. A variety of tools keeps their user hydrated and fit,
              but also make them buy in one click and sign licence agreements
              without reading. As complex interfaces have moved to intuitive
              navigation and user-friendly experiences, the habit of thinking
              about the final purpose of navigation through a menu or choosing
              an option has become less important. Would lifelogging tools have
              been so widely popular if a user would have to open five menus in
              order to get to the one she needs?
            </p>

            <p>
              It is possible that coexisting with machine intelligence means
              being guided and presented with ready-made options. In that
              perspective, randomness is liberating. It presents additional
              opportunities to satisfy our curiosity.
            </p>

